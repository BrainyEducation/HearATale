                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Nineteenth-century Americans in all parts of the country, particularly from around the 1830s to the 1880s, delighted in exploring the wide varieties of American speech.  Long before Edison, writers pioneered audio-recording via dialectical spelling which suggested to readers how people throughout the country sounded, particularly those–red, white, black, and brown–in relatively isolated rural areas. </p>
