                        <h3> Assisted Reading </h3>
			<p>Some of us struggled to understand and appreciate Shakespeare, Milton, 
Pope, and other poets, in part because of archaic vocabulary, but more often—though we didn’t realize it—because as eye-readers we couldn’t hear the vocal inflections, and failed to appreciate the extent to which ear-reading can make challenging language much easier to understand.  On several occasions I’ve asked college students to silently read a dozen or 
so lines from <em>Paradise Lost</em> and then raise their hands if they felt they understood it. Out of a class of 25 maybe 3 or 4 would volunteer (and some of those were overly ambitious hands). I then asked them to close their
eyes and played audio of Jim Garvey reading another dozen or so lines, and this time about half the class would raise their hands. Then I asked them to silently read another dozen lines as they listened to the audio for those same lines, and usually about 80% or 90% of the class would raise their hands. When we can hear great poetry read aloud, when we can 
imagine hearing great poetry read aloud, we can understand it far better. 
Struggling readers can have the same experience of prose—if they’re 
interested in the material. Children who heard the stories available now in the children’s section later became much better readers than their peers (visit the About material for the Children’s section). Middle school and high school students who listen to audio while reading the text of works like <em>Alice in Wonderland</em> or <em>Narrative of the Life of Frederick Douglass</em> or <em>The Red Badge of Courage</em> or <em>The Awakening</em> or <em>Candide</em> or <em>Huckleberry Finn</em> 
or “The Fall of the House of Usher” or “The Notorious Jumping Frog of Calaveras County” or numerous other works on this site will likewise begin to develop an internal ear which makes reading comprehension far easier—and far more enjoyable and rewarding. </p>
